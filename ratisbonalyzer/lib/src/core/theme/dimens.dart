/// A central class for reusable dimensions across the app.
abstract final class Dimens {
  /// Standard padding used for screen edges and large containers.
  static const double paddingLarge = 16.0;

  /// Medium padding used for list items and widget spacing.
  static const double paddingMedium = 12.0;

  /// Small padding used for internal widget spacing.
  static const double paddingSmall = 8.0;

  /// Tiny padding for minimal spacing.
  static const double paddingTiny = 4.0;

  /// Default leading width for AppBars with a logo.
  static const double appBarLeadingWidth = 80.0;

  /// Standard RVV logo height.
  static const double rvvLogoHeight = 64.0;

  /// Standard border radius for cards and buttons.
  static const double borderRadiusMedium = 12.0;
}
